package calculator

func Add(a, b int) (c int) {
        c = a + b
        return
}

func Sub(a, b int) (c int) {
        c = a - b
        return
}
func Mutiply(a, b int) (c int) {
        c = a * b
        return
}

func Divide(a, b int) (c int) {
        c = int(a / b)
        return
}

func Modulo(a, b int) (c int) {
        c = int(a % b)
        return
}

